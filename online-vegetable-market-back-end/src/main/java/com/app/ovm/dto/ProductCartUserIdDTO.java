package com.app.ovm.dto;

public class ProductCartUserIdDTO {
	private int userId;
	private int productId;

	public ProductCartUserIdDTO() {

	}

	public ProductCartUserIdDTO(int userId, int productId) {

		this.userId = userId;
		this.productId = productId;
	}

	public int getUserId() {
		return userId;
	}

	public void setUserId(int userId) {
		this.userId = userId;
	}

	public int getproductId() {
		return productId;
	}

	public void setproductId(int productId) {
		this.productId = productId;
	}

	@Override
	public String toString() {
		return "UserIdProductIdDTO [userId=" + userId + ", productId=" + productId + "]";
	}
}